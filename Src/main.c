void showTimeCalender(RTC_TimeTypeDef t,RTC_DateTypeDef d, int counter){
	HAL_RTC_GetTime(&hrtc,&t,RTC_FORMAT_BIN);
	HAL_RTC_GetDate(&hrtc,&d,RTC_FORMAT_BIN);
		
	char indicator[2];
	char time[9];
	char date[16];
	if(t.Hours - 12 >= 0){
		t.Hours = t.Hours-12;
		strcpy(indicator,"PM");
	}
	else{
		strcpy(indicator,"AM");
	}
	strcpy(time," ");
	strcat(time,zeropadd(t.Hours));
	strcat(time,":");
	strcat(time,zeropadd(t.Minutes));
	strcat(time,":");
	strcat(time,zeropadd(t.Seconds));
	strcat(time," ");
	if(counter < 9 && abs(t.Seconds - b) > 0.25){
		*(time + counter) = ' ';
		b = t.Seconds;
	}
	
	setCursor(3,0);
	print(time);
	print(indicator);

	char calender[2][10] ;
	sprintf(calender[0], "%d", d.Date);
	sprintf(calender[1], "%d", d.Year);
	strcpy(date," ");
	strcat(date,toWeekday(d.WeekDay));
	strcat(date," ");
	strcat(date,toMonth(d.Month));
	strcat(date," ");
	strcat(date,calender[0]);
	strcat(date,",20");
	strcat(date,calender[1]);
	strcat(date," ");
	setCursor(2,1);
	
	if(counter > 8 && abs(t.Seconds - b) > 0.25){
		counter %= 8;
		
		switch (counter){	
			case 1:
				*(date + counter + 9) = ' ';
			break;
			case 3:
				*(date + counter) = ' ';
				*(date + counter - 1) = ' ';
				*(date + counter - 2) = ' ';
			break;
				
			case 4:
				*(date + counter + 11) = ' ';
			break;
			
			case 6:
				*(date + counter - 1) = ' ';
				*(date + counter) = ' ';
				*(date + counter + 1) = ' ';
			break;
			
		}
			
		
		b = t.Seconds;
	}
	
	print(date);
}

